    private void brnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_brnSalvarActionPerformed
        String n, i, c;
   n = txtNome.getText();
   i = txtIdade.getText();
   c = txtCpf.getText();
   
   try{
   Class.forName("com.mysql.cj.jdbc.Driver");
   Connection conectado = DriverManager.getConnection
        ("jdbc:mysql://localhost:3306/projeto","root","admin");
   PreparedStatement st = conectado.prepareStatement
        ("INSERT INTO pacientes(nome_paciente,idade,cpf) VALUES(?,?,?)");
   st.setString(1, n);
   st.setString(2, i);
   st.setString(3, c);
   st.executeUpdate();
    JOptionPane.showMessageDialog(null, "Paciente Cadastrado com Sucesso");
   } catch (ClassNotFoundException ex){
        JOptionPane.showMessageDialog(null, "Driver m√£o colocado na library");
   } catch (SQLException ex){
        JOptionPane.showMessageDialog(null, "Erro nos dados do banco de dados"+ ex.getMessage());}{
    }
    txtNome.setText("");
    txtIdade.setText("");
    txtCpf.setText("");
    }//GEN-LAST:event_brnSalvarActionPerformed

